#!/usr/bin/env python3
"""Platform Report Templates — render bug bounty reports for HackerOne, Bugcrowd, or generic disclosure.

Usage:
    platform_templates.py render --finding-dir evidence/finding_001 --platform hackerone --output report.md
    platform_templates.py list-fields --platform hackerone
"""

import argparse
import json
import re
import sys
from datetime import datetime, timezone
from pathlib import Path
from typing import Dict, Any


def now_iso() -> str:
    return datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")


def now_date() -> str:
    return datetime.now(timezone.utc).strftime("%B %d, %Y")


